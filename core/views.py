    s_body_flat = ParagraphStyle(
        "BodyFlat", fontName=font, fontSize=14, leading=21,
        alignment=TA_JUSTIFY, spaceAfter=0,
    )
    # Маркированный список: висячий отступ 0,5 см (первая 0 → следующие 0,5)
    s_bullet = ParagraphStyle(
        "Bullet", fontName=font, fontSize=14, leading=21,
        alignment=TA_LEFT,
        leftIndent=LIST_INDENT,
        firstLineIndent=-LIST_INDENT,
        spaceAfter=0,
    )
    # Нумерованный список: висячий отступ 0,5 см
    s_numbered = ParagraphStyle(
        "Numbered", fontName=font, fontSize=14, leading=21,
        alignment=TA_JUSTIFY,
        leftIndent=LIST_INDENT,
        firstLineIndent=-LIST_INDENT,
        spaceAfter=0,
    )
    # Примечание: курсив, по левому, с отступом 1,25 см

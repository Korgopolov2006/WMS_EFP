@login_required
def user_manual_download(request: HttpRequest) -> HttpResponse:
    context = _build_manual_context(request)
    role_map = {item["code"]: item for item in context["role_cards"]}

    requested_role = (request.GET.get("role") or "").strip().upper()
    user_role = getattr(request.user, "role", "")
    if requested_role in role_map:
        selected_role_code = requested_role
    elif user_role in role_map:
        selected_role_code = user_role
    else:
        selected_role_code = next(iter(role_map.keys()))

    selected_role = role_map[selected_role_code]
    pdf_content = _build_manual_pdf(
        role=selected_role,
        workflow_steps=context["workflow_steps"],
        work_principles=context["work_principles"],
    )

    response = HttpResponse(pdf_content, content_type="application/pdf")

    # Имя файла на русском — читаемое для пользователя.
    # Формат: «Инструкция_сотрудника_<Роль>_<ДД.ММ.ГГГГ>.pdf»
    role_label = selected_role.get("role", selected_role_code)
    # Убираем пробелы и небезопасные символы из подписи роли
    safe_role = role_label.replace(" ", "_").replace("/", "-").replace("\\", "-")
    date_str = timezone.now().strftime("%d.%m.%Y")
    filename_rus = f"Инструкция_сотрудника_{safe_role}_{date_str}.pdf"

    # ASCII-fallback на случай старых клиентов (RFC 6266).
    filename_ascii = f"manual_{selected_role_code.lower()}_{timezone.now().strftime('%Y%m%d')}.pdf"

    # RFC 5987: имя файла в UTF-8 через filename*
    quoted = urllib.parse.quote(filename_rus, safe="")
    response["Content-Disposition"] = (
        f'attachment; filename="{filename_ascii}"; filename*=UTF-8\'\'{quoted}'
    )
    return response



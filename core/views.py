def _normalize_text_for_gost(text: str) -> str:
    """
    Чистит текст под правила русской типографики ГОСТ 2.105-95:
      • двойные пробелы → одинарный;
      • дефис внутри предложения между пробелами → длинное тире «—»;
      • прямые кавычки "..." → «ёлочки».
    """
    if not text:
        return ""
    s = str(text)
    # Двойные пробелы → одинарный
    while "  " in s:
        s = s.replace("  ", " ")
    # " - " → " — " (дефис между пробелами → длинное тире)
    s = s.replace(" - ", " — ")
    # Прямые кавычки → ёлочки (только если их чётное число)
    if s.count('"') >= 2 and s.count('"') % 2 == 0:
        result = []
        opening = True
        for ch in s:
            if ch == '"':
                result.append("«" if opening else "»")
                opening = not opening
            else:
                result.append(ch)
        s = "".join(result)
    return s


def _strip_trailing_period(text: str) -> str:
    """Убирает завершающую точку (для последующей замены на ; или .)."""
    t = text.rstrip()
    if t.endswith("."):
        return t[:-1].rstrip()
    return t


